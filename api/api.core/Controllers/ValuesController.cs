using Microsoft.IdentityModel.Tokens;
using System.Text;
using System.IdentityModel.Tokens.Jwt;
using api.core.Utils;
using Microsoft.AspNetCore.Authentication.JwtBearer;

namespace api.core.Controllers
{    
    [Authorize(Policy ="Admin")]
    [Route("api/[controller]")]    
    public class ValuesController : Controller
    {
        // GET api/values
        [HttpGet]
        public IEnumerable<string> Get()
        {
            return new string[] { "value1", "value2" };
        }

        // GET api/values/5
        [HttpGet("{id}")]
        public string Get(int id)
        {
            return "value";
        }

        // POST api/values
        [HttpPost]
        public void Post([FromBody]string value)
        {
        }

        // PUT api/values/5
        [HttpPut("{id}")]
        public void Put(int id, [FromBody]string value)
        {
        }

        // DELETE api/values/5
        [HttpDelete("{id}")]
        public void Delete(int id)
        {
        }

        [AllowAnonymous]
        [HttpPost]
        [Route("token")]
        public IActionResult RequestToken()
        {            
            var claims = new[]
            {
                new Claim(ClaimTypes.Name, "username")
            };

            var key = new SymmetricSecurityKey(Encoding.UTF8.GetBytes(Configuration.Config["TokenAuthentication:SecretKey"]));
            var creds = new SigningCredentials(key, SecurityAlgorithms.HmacSha256);

            var token = new JwtSecurityToken(
                issuer: Configuration.Config["TokenAuthentication:Issuer"],
                audience: Configuration.Config["TokenAuthentication:Audience"],
                claims: claims,
                expires: DateTime.Now.AddMinutes(30),
                signingCredentials: creds);

            return Ok(new
            {
                token = new JwtSecurityTokenHandler().WriteToken(token)
            });
        }
    }
}
